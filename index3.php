<?php

$nilaiAwal = 0;
$nilaiAkhir = 10;

// while
echo 'menggunakan While<br />';
while($nilaiAwal < $nilaiAkhir) {
        echo 'Hello World';
        echo '<br />';
        $nilaiAwal++;
}

// for
echo '<br />menggunakan For<br />';
for($i = 0; $i < 10; $i++) {
        echo 'Hello World';
        echo '<br />';
}